package chapter4_Programming_with_classes.Task7;

public final class Mediana {

        private final double x;
        private final double y;

        public Mediana(double x, double y) {
            this.x = x;
            this.y = y;
        }

        public double getX() {
            return this.x;
        }

        public double getY() {
            return this.y;
        }
}
